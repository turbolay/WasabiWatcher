class TimeCount:
    def __init__(self, time, count) -> None:
        self.time = time
        self.count = count

class TimeCoin:
    def __init__(self, time, coin) -> None:
        self.time = time
        self.coin = coin

class TimeOutput:
    def __init__(self, time, output) -> None:
        self.time = time
        self.output = output